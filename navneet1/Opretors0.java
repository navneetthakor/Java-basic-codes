public class Opretors0 {

	public static void main(String[] args) {
		int x = 51;
		int y = 50;
		
		System.out.println("The Mr. X is " + x + " And the Mr. Y is " + y );
		System.out.println("x + Y = " + (x+y) );
		System.out.println("x - Y = " + (x-y) );
		System.out.println("x * Y = " + (x*y) );
		System.out.println("x / Y = " + (x/y) );
		System.out.println("x % Y = " + (x%y) );
	}

}
